//Different datatypes in javaScript

//Numbers
let x=70;
console.log("data type of x is: "+typeof(x)+" & value of x: "+x);

//String
let y="Debabrat Adhikary";
console.log("data type of y is: "+typeof(y)+" & value of y: "+y);

//Boolean
let z=true;
console.log("data type of z is: "+typeof(z)+" & value of z: "+z);

//UnDefined
let a;
console.log("data type of a is: "+typeof(a)+" & value of a: "+a);

//Null
let b=null;
console.log("data type of b is: "+typeof(b)+" & value of b: "+b);